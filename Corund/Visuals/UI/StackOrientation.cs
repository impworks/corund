namespace Corund.Visuals.UI
{
    /// <summary>
    /// Direction of a StackPanel's growth.
    /// </summary>
    public enum StackOrientation
    {
        /// <summary>
        /// New items are added to the bottom.
        /// </summary>
        Down = 0,

        /// <summary>
        /// New items are added to the right.
        /// </summary>
        Right = 1,

        /// <summary>
        /// New items are added to the left.
        /// </summary>
        Left = 2,

        /// <summary>
        /// New items are added to the top.
        /// </summary>
        Up = 3
    }
}
